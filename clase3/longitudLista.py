# Lista vacia
# La longitud de una lista vacia
lista_vacia = []
print("Logitud de una lista vacia: ", len(lista_vacia))

# Lista con enteros
# La longitud es igual al numero de elementos que tiene la lista
lista_enteros = [1, 2, 3, 4, 5]
print("Logitud de una lista de entros es de: ", len(lista_enteros))

# Lista con cadenas
# La longitud es igual al numero de elementos de la lista
lista_cadenas = ['manzana', 'banana', 'cereza']
print("Logitud de una lista de cadenas: ", len(lista_cadenas))

# Lista mixta
# La longitud cuenta todos los elementos (sin importar el tipo de elemento)
lista_mixta = [1, 'texto', 3.14, [1, 2, 3]]
print("Logitud de una lista mixta: ", len(lista_mixta))
print("Logitud de una lista mixta: ", len(lista_mixta[3]))



#Lista conelementos repetidos
# La longitd es el resultado de multiplicar el numero de elementos repetidos
lista_repetidos = [0] * 10
print("Logitud de una lista de numeros repetidos: ", len(lista_repetidos))

