# Ejercicio 3: Desempaquetado de Tuplas
# • Crea una tupla llamada información con los siguientes elementos: (25, "Ana", "Ingeniera").
# • Desempaqueta la tupla en tres variables: edad, nombre y profesión.
# • Muestra el valor de cada variable

informacion = (25, "Ana", "Ingeniera")

edad, nombre, profesion = informacion

print(f"Edad: {edad}")
print(f"Nombre: {nombre}")
print(f"Profesión: {profesion}")